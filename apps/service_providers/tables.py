from django.conf import settings
from django.urls import reverse
from django_tables2 import tables

from apps.generics import actions
from apps.service_providers.models import LlmProviderModel, VoiceProviderType


def make_table(provider_type: str, label: str, model, fields=("type", "name"), extra_actions=None):
    meta_attrs = {
        "model": model,
        "fields": fields,
        "row_attrs": settings.DJANGO_TABLES2_ROW_ATTRS,
        "orderable": False,
    }
    Meta = type("Meta", (object,), meta_attrs)

    class_name = model.__name__ + "AutogeneratedTable"
    table_actions = [
        actions.edit_action(url_name="service_providers:edit", url_factory=_make_url_factory(provider_type)),
        *(extra_actions or []),
        actions.delete_action(
            url_name="service_providers:delete",
            url_factory=_make_url_factory(provider_type),
            confirm_message=f"This will remove the {label} from any places it is being used",
        ),
    ]
    table_class_attrs = {
        "actions": actions.ActionsColumn(actions=table_actions),
        "Meta": Meta,
    }
    return type(tables.Table)(class_name, (tables.Table,), table_class_attrs)


def _manage_voices_url_factory(url_name, request, record, value):
    return reverse(url_name, args=[request.team.slug, record.pk])


def make_voice_table(provider_type: str, label: str, model, fields=("type", "name")):
    manage_voices_action = actions.Action(
        url_name="service_providers:custom_voice_list_voices",
        url_factory=_manage_voices_url_factory,
        label="Voices",
        icon_class="fa-solid fa-microphone",
        title="Manage custom voices",
        display_condition=lambda request, record: record.type == VoiceProviderType.openai_custom_voice,
    )
    return make_table(provider_type, label, model, fields, extra_actions=[manage_voices_action])


def _make_url_factory(provider_type):
    def url_factory(url_name, request, record, value):
        return reverse(url_name, args=[request.team.slug, provider_type, record.pk])

    return url_factory


class LlmProviderModelTable(tables.Table):
    actions = actions.ActionsColumn(
        actions=[
            actions.edit_action(url_name="service_providers:llm_provider_model_edit"),
            actions.delete_action(
                url_name="service_providers:llm_provider_model_delete",
                confirm_message="Are you sure you want to delete this custom LLM Provider Model?",
                display_condition=lambda request, record: not record.has_related_objects(),
            ),
        ]
    )

    class Meta:
        model = LlmProviderModel
        fields = ("name", "type", "max_token_limit")
        row_attrs = settings.DJANGO_TABLES2_ROW_ATTRS
        empty_text = "No custom llm provider models found."
